# ── Companion planting relationships ─────────────────────────────────────────
# One row per pair — Plant model queries both directions so we never duplicate.
def companion!(name1, name2, relationship, notes)
  p1 = Plant.find_by!(name: name1)
  p2 = Plant.find_by!(name: name2)
  existing = PlantCompanion.find_by(plant: p1, companion: p2) ||
             PlantCompanion.find_by(plant: p2, companion: p1)
  if existing
    existing.update!(relationship: relationship, notes: notes)
  else
    PlantCompanion.create!(plant: p1, companion: p2, relationship: relationship, notes: notes)
  end
end

